# Escreva um programa que leia 6 notas diferentes e faça a média do aluno

Nota_1 = int(input('Espanhol: '))
Nota_2 = int(input('Quimica: '))
Nota_3 = int(input('Fisica: '))
Nota_4 = int(input('Portugues: '))
Nota_5 = int(input('Ingles: '))
Nota_6 = int(input('Matematica: '))

Media = int((Nota_1 + Nota_2 + Nota_3 + Nota_4 + Nota_5 + Nota_6)/6)

print(f'A média é de {Media}')